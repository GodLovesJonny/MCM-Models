'''
max:    z = 4x1 + 3x2
st:     2x1 + 3x2<=10
        x1 + x2 <=8
        x2 <= 7
        x1,x2 > 0
'''

from scipy.optimize import linprog

c = [4, 3]
A = [[2, 3], [1, 1]]
b = [10, 8]
x1_bound = [0, None]
x2_bound = [0, 7]
res = linprog(c, A, b, bounds=(x1_bound, x2_bound))
print(res)
